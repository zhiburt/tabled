use tabled::tables::iter::IterTable;
use util::{create_matrix, test_table};

test_table!(
    iter_table,
    IterTable::new(create_matrix::<3, 3>()).to_string(),
    "+-----+-----+-----+"
    "| 0-0 | 0-1 | 0-2 |"
    "+-----+-----+-----+"
    "| 1-0 | 1-1 | 1-2 |"
    "+-----+-----+-----+"
    "| 2-0 | 2-1 | 2-2 |"
    "+-----+-----+-----+"
);

